using System;

namespace _2_CriandoVariaveis
{
    class Program
    {
        static void Main(string[] args)
        {
            int idade = 50;
            Console.WriteLine("A idade agora é " + idade);

            idade = 25;
            Console.WriteLine("A idade agora é " + idade);

            idade = 25 * 2;
            Console.WriteLine("A idade agora é " + idade);

            Console.ReadLine();
        }
    }
}
